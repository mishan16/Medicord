class Event{
  final String date;
  final String doctor;
  final String  hospital;
  final bool eventCompletion;
  final String image;
  final String location;
  final String note;
  final String specialization;
  final String time;
  final String year;
  final String visitReason;
  final int visitType;

  Event(this.date, this.doctor, this.hospital, this.eventCompletion, this.image, this.location, this.note, this.specialization, this.time, this.year, this.visitReason, this.visitType);
}